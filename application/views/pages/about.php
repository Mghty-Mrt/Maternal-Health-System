    window.onload = function() {
        const img = document.getElementById('sourceImage');
        const canvas = document.getElementById('trimmedCanvas');
        const ctx = canvas.getContext('2d');

        img.onload = () => {
            // Set canvas dimensions to match the image
            canvas.width = img.width;
            canvas.height = img.height;

            // Draw the image on the canvas
            ctx.drawImage(img, 0, 0);

            // Calculate the coordinates for trimming (bottom left to top right by 10%)
            const trimAmount = Math.min(canvas.width, canvas.height) * 0.1;
            const startPoint = {
                x: 0,
                y: canvas.height
            };
            const endPoint = {
                x: canvas.width,
                y: 0
            };

            // Create a clipping path to trim the image
            ctx.beginPath();
            ctx.moveTo(startPoint.x, startPoint.y);
            ctx.lineTo(startPoint.x + trimAmount, startPoint.y - trimAmount);
            ctx.lineTo(endPoint.x - trimAmount, endPoint.y + trimAmount);
            ctx.lineTo(endPoint.x, endPoint.y);
            ctx.closePath();
            ctx.clip();

            // Draw the trimmed image
            ctx.drawImage(img, 0, 0);

            // Get the trimmed image data
            const trimmedImageData = canvas.toDataURL('image/jpeg');

            // Display the trimmed image
            img.src = trimmedImageData;
        };
    };
